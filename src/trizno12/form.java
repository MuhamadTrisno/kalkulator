    public void kuadrat(int bilangan1, int bilangan2) {
        double hasil = 0;
        hasil = Math.pow(bilangan1, bilangan2);
        txthasil.setText(hasil+"");
        
    }

    public void akar(int bilangan1) {
        double hasil = 0;
        hasil = Math.sqrt(bilangan1);
        txthasil.setText(hasil+"");
        
    }
    public void penjumlahan(Double bilangan1, Double bilangan2) {
        double hasil = 0;
        hasil = bilangan1 + bilangan2;
        txthasil.setText(hasil+"");
        
    }
    public void pengurangan(int bilangan1, int bilangan2) {
        int hasil = 0;
        hasil = bilangan1 - bilangan2;
        txthasil.setText(hasil+"");
        
    }
    public void perkalian(int bilangan1, int bilangan2) {
        int hasil = 0;
        hasil = bilangan1 * bilangan2;
        txthasil.setText(hasil+"");
        
    }
    public void pembagian(int bilangan1, int bilangan2) {
        float hasil = 0;
        hasil = bilangan1 / bilangan2;
        txthasil.setText(hasil+"");
        
    }
    public void modulus(int bilangan1, int bilangan2) {
        int hasil = 0;
        hasil = bilangan1 % bilangan2;
        txthasil.setText(hasil+"");
        
    }
    
